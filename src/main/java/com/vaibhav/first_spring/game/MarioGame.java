package com.vaibhav.first_spring.game;

public class MarioGame {
    public void up() {
        System.out.println("Jump");
    }

    public void down() {
        System.out.println("Duck");
    }

    public void left() {
        System.out.println("Go back");
    }

    public void right() {
        System.out.println("Go forward");
    }
}
